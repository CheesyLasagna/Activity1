public class Javaapplication 
{
    public static void main(String[] args) 
    {
        for(int i=4; i >= 1; --i)
        {
            for(int j = 1; j <= i; ++j)
            {
            System.out.print("* ");
            }
            System.out.println();
        }
         for(int x = 1; x <= 4; ++x) 
         {
            for(int y = 1; y <= x; ++y)
            {
                System.out.print("** ");
            }
            System.out.println();
            }
    } 
     
}
